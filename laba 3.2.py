#Создать (не программно) текстовый файл со следующим содержимым:
#One — 1
#Two — 2
#Three — 3
#Four — 4
#Напишите программу, открывающую файл на чтение и считывающую построчно данные.
#При этом английские числительные должны заменяться на русские.
#Новый блок строк должен записываться в новый текстовый файл.

# Словарь для замены английских числительных на русские
numbers_dict = {
    'One': 'Один',
    'Two': 'Два',
    'Three': 'Три',
    'Four': 'Четыре'
}

with open('input.txt', 'r') as input_file:
    with open('output.txt', 'w') as output_file:
        for line in input_file:
            # Разделяем строку на слова
            words = line.strip().split(' — ')
            # Если первое слово есть в словаре, заменяем его на русское числительное
            if words[0] in numbers_dict:
                words[0] = numbers_dict[words[0]]
            # Записываем строку с изменениями в новый файл
            output_file.write(' — '.join(words) + '\n')

print("Замена выполнена, результат записан в файл output.txt.")